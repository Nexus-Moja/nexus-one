import assert from 'node:assert/strict';
import { hashPassword, verifyPassword, tokenDigest } from '../src/server/security.mjs';
import { allowRequest } from '../src/server/rate-limit.mjs';
const encoded = hashPassword('A-strong-demo-password!');
assert.equal(verifyPassword('A-strong-demo-password!', encoded), true);
assert.equal(verifyPassword('wrong-password', encoded), false);
assert.equal(tokenDigest('abc').length, 64);
for (let i=0;i<3;i++) assert.equal(allowRequest('foundation-test', 3, 10000).allowed, true);
assert.equal(allowRequest('foundation-test', 3, 10000).allowed, false);
console.log('Production foundation security tests passed.');
